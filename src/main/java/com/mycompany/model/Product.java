
package com.mycompany.model;

/**
 *
 * @author truongthanh
 */
public class Product {

    public int getIdProduct() {
        return idProduct;
    }

    public void setIdProduct(int idProduct) {
        this.idProduct = idProduct;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public int getProductQuantity() {
        return productQuantity;
    }

    public void setProductQuantity(int productQuantity) {
        this.productQuantity = productQuantity;
    }

    public String getProductUrlImage() {
        return productUrlImage;
    }

    public void setProductUrlImage(String productUrlImage) {
        this.productUrlImage = productUrlImage;
    }

    public int getIdProductType() {
        return idProductType;
    }

    public void setIdProductType(int idProductType) {
        this.idProductType = idProductType;
    }

    public String getProductSize() {
        return productSize;
    }

    public void setProductSize(String productSize) {
        this.productSize = productSize;
    }

    public float getProductRentalPrice() {
        return productRentalPrice;
    }

    public void setProductRentalPrice(float productRentalPrice) {
        this.productRentalPrice = productRentalPrice;
    }

    public String getProductDescription() {
        return productDescription;
    }

    public void setProductDescription(String productDescription) {
        this.productDescription = productDescription;
    }

    public float getProductPrice() {
        return productPrice;
    }

    public void setProductPrice(float productPrice) {
        this.productPrice = productPrice;
    }

    public float getProductWeight() {
        return productWeight;
    }

    public void setProductWeight(float productWeight) {
        this.productWeight = productWeight;
    }

    private int idProduct;
    private String productName;
    private int productQuantity;
    private String productUrlImage;
    private int idProductType;
    private String productSize;
    private float productRentalPrice;
    private String productDescription;
    private float productPrice;
    private float productWeight;
}
